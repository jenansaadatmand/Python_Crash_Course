# Favourite Book: write a function called favourite_book() that accepts one parameter, title
# The function should print a message, such as One of my favorite books is Alice in Wonderland
# Call the function, making sure to include a book title as argument in the function call

def favourite_book(title):
    """Display a message about someone's favourite book"""
    print(f"{title} is one of my favourite books.")
favourite_book('alice in wonderland')
print()
